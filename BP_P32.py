# set in Python

# create set
set1 = set()
print("set1 type: ", type(set1))

# add items in set
set1.add(60)
print("set1 is: ", set1)

set1.add(50)
print("set1 is: ", set1)

set1.add(70)
print("set1 is: ", set1)

# Auto manage number in ascending order

set1.discard(60)
print("After remove item from set1 is: ", set1)

# add() method used to add items in set
# discard() method is used to remove items from set

